<H1>Réalisations:<H1>

1- mémorisation du Saint Coran par plus de 400 élèves.
2- Application du programme du Ministère de l’Education nationale en enseignement Franco-Arabe dans les 4 cycles : préscolaire, élémentaire, moyen et secondaire.
3- Inscription des bacheliers dans les universités( Cheikh Anta Diop de Dakar ,Gaston Berger de Saint-louis , Oumou al-quraa (Mecque), l’université al-imam de Riyad, l’université al-amira Noura des femmes à Riyad, l’université Mouhamed V de Rabat, l’université de Marrakech, l’université de Koweit, l’université d’Istanbul (Turquie), l’université internationale d’Afrique (Soudan) et l’université internationale islamique de Malaisie.
Il est à noter que l’année dernière nous avions obtenu 26 bourses d’excellence de ces universités.
4 – Attribution de notre établissement comme waqf pour assurer la pérennité de son action et le renouveau de son expression.
5/ Existence d’une équipe pédagogique, compétente, qualifiée et formée dans les écoles de formation du pays.
6/ Participation à la politique nationale de création d’emplois pour les citoyens. Nous avons 85 employés.
7/- Résolution au paiement des impôts annuels au Ministère des finances et versement de cotisations à la caisse de sécurité sociale et à l’IPRES.
8/-Etablissement d’un climat de confiance chez les parents d’élèves envers l’institution qui a donné lieu à une forte affluence, à tel point que la liste d’attente est devenue très longue.
9/- utilisation des nouvelles technologies dans l’administration scolaire et à la recherche scientifique en mettant à la disposition des élèves une salle informatique et une bibliothèque.
10/- ouverture d’une bibliothèque scolaire où se forment les élèves du secondaire en recherches, sous la supervision du professeur encadreur.
11/ Fourniture de jeux et d’équipements scolaires dont ont besoin les enfants du jardin.
12/ Solidification des relations avec les autorités de la localité et les différents agents de la collectivité locale.
13/ Organisation des dizaines de séminaires pour la formation de l’équipe pédagogique et la modernisation de leurs outils de travail.
14/ Reconnaissance de l’Institution par l’Etat via récépissé du Ministère de l’intérieur, autorisation du Ministère de l’Education Nationale et ONG au Sénégal
15/ Dénomination officielle du quartier de l’institution (cité daroul imane) par l’ex-maire vu son rôle dans la localité.
16/ Partenariat avec l’IFE de l’UCAD pour organiser des cours intensifs afin de relever le niveau de la langue française chez les enseignants et fonctionnaires arabophones de l’institut.
17-Inscription de plus de 1300 élèves dans nos établissements.
 Réalisations dans le social:

1- Distribution annuelle de 24 tonnes de dattes, depuis 10 ans, dans des mosquées et des écoles coraniques, aux nécessiteux sans distinction entre les musulmans
2- Parrainage des orphelines et orphelins en collaboration avec l’Association de la renaissance du patrimoine et de l’expansion de la culture arabe au Sénégal.
3- Distribution des fournitures scolaires et des habits aux élèves des écoles publiques dans les banlieues dakaroises.
4- Formation d’un comité social permanent dans la mosquée pour satisfaire les besoins des populations et résoudre leurs problèmes chaque semaine après la prière de vendredi.
5- Inscription gratuite de 5% des enfants des démunis.
Projets futurs:

1- Achat d’un terrain à Diass à côté du nouvel aéroport pour construire un grand complexe éducatif qui englobera le cycle primaire jusqu’en doctorat.
2- Accord de partenariat académique avec l’université internationale d’Afrique pour ouvrir une faculté d’éducation à partir de l’année 2016 .
3- Ouverture des annexes dans les localités, comme Tarédji, département de Podor, où nous avons acheté un terrain afin de construire un institut islamique pour les fils de la localité.

